from typing import List 

def sort_colors(nums: List[int]) -> None:
    """
     Do not return anything, modify nums in-place instead.
    """
    return nums


# Input: nums = [2,0,2,1,1,0]
# Output: [0,0,1,1,2,2]
print(sort_colors([2,0,2,1,1,0]))


# Input: nums = [2,0,1]
# Output: [0,1,2]
print(sort_colors([2,0,1]))